                <flux:heading size="lg" level="3" class="mb-5">1939-1946</flux:heading>
                Whilst the major effort was in treating the vast number of new recruits, personnel were also deployed
                overseas to garrison dental centres as well as in large numbers to field ambulances, casualty clearing
                stations, hospitals, field dental centres, field dental laboratories and mobile dental units. A number
                of personnel were killed or wounded in action and others were taken prisoner of war. Two dental officers
                ended up in Colditz and Pte Coe, a dental mechanic, successfully escaped from a Camp in Poland to become
                the first British Prisoner of War to escape from a WW2 PoW Camp and complete the ‘home run’ back to the
                UK. He was awarded the DCM at a private audience with the King. Four dental officers won the Military
                Cross for their actions during the invasion of Sicily; the beach landings on D-Day; crossing the Rhine
                on Operation PLUNDER; and in the battle of Kohima.

                In recognition for its services during the war, in 1946, the Corps was granted the Regimental Honour of
