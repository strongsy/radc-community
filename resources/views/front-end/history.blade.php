                <flux:heading size="lg" level="3" class="mb-5">Pre 1921</flux:heading>
                As far back as the seventeenth century instruments for the extraction and scaling of teeth had been
                included in military surgeons’ chests, but there were to be no uniformed dentists in the Army until
                after the start of the First World War. The Army deployed to France in 1914 without any dental officers,
                however, within two months of General Haigh suffering toothache during the first battle of the Aisne in
                September 1914, six dental officers had been commissioned and sent to France. By the end of the year
                twenty dental officers were serving in France and by the end of the war 892 Army dental officers were
                looking after the needs of both the Army and the newly formed RAF.

                On 4th January 1921, King George V signed the Royal Warrant authorising the formation of The Army Dental
                Corps (AD Corps).
                Demobilisation saw most dental officers return to civilian practice. Their need in the Army, however,
                had been established. In 1919 The British Dental Association lobbied the War Office for the formation of
                a Dental Corps and proposed the structure for such a Corps. This was accepted by the War Office and the
                Treasury. In February 1920, the Secretary of State for War, Winston Churchill, presented the Army
                estimates for 1920/21 to Parliament, including the intention to form an Army Dental Corps.

                On 4th January 1921, King George V signed the Royal Warrant authorising the formation of The Army Dental
                Corps (AD Corps).
