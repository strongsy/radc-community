                <flux:heading size="lg" level="3" class="mb-5">1990-2020</flux:heading>
                Whilst the fall of the Berlin wall signalled the end of the Cold War, conflicts elsewhere in the world
                soon saw the RADC deployed in other Theatres in support of the Army in the First Gulf War, Iraq and
                Afghanistan as well as in peacekeeping operations in Angola, the Balkans and South Sudan and on
                humanitarian aid deployments such as the Ebola outbreak in Sierra Leone.

                Recent events have also seen them involved with the military and NHS response to the Covid-19 crisis.
                In 1996 the Headquarters of the three Service Dental Branches combined to form the Defence Dental Agency
                (DDA). In the early 2000s the DDA was changed to the Defence Dental Services under the Surgeon General
                and after further restructuring now forms the dental element of Defence Primary Healthcare (DPHC(D)).
                Throughout these changes the RADC and its RN and RAF colleagues have retained their single Service
                identities whilst benefitting from efficiencies in combined training, policy making and resourcing. RADC
                personnel not only continue to serve in the Firm Base as part of DPHC(D) but also in Medical Regiments
                of the Field Army where they occupy both clinical and command/staff appointments.
