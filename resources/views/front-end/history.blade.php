                <flux:heading size="lg" level="3" class="mb-5">1946-1990</flux:heading>
                In 1948, with the advent of the NHS, the Corps’ role was expanded to include dental care to military
                families and other entitled civilians stationed overseas. During the Cold War, with the Army’s focus on
                NW Europe, a large proportion of the Corps was to be stationed in Germany for the next 60 years. Outside
                NW Europe the last 70+ years has seen the RADC deployed in support of the Army on a wide variety of
                operations including Palestine, the Malayan Emergency, Korean War, Mau Mau Uprising, Cyprus, Suez
                Crisis, Brunei Revolt, Borneo confrontation, Aden, Northern Ireland, Falklands War.

                Whilst the fall of the Berlin wall signalled the end of the Cold War, conflicts elsewhere in the world
