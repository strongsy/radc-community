                <flux:heading size="lg" level="3" class="mb-5">1921-1939</flux:heading>
                Through the provision of dental treatment and education in oral hygiene the dental condition of the Army
                began to show marked improvement. The first operational deployments for members of the Corps came
                immediately in the form of the 1921-24 Waziristan Campaign in the Indian North-West Frontier Province
                followed in 1927 with the deployment of dental teams as part of the Shanghai Defence Force. 1928 saw the
                first dental officer commissioned into the TA and in 1930 the responsibility for the RAF ceased with the
                formation of the RAF Dental Branch and the transfer of a number of personnel to the RAF in order to form
                the nucleus of the new Branch.

                In recognition for its services during the war, in 1946, the Corps was granted the Regimental Honour of
                ROYAL from which time it became the Royal Army Dental Corps (RADC).
                By 1939 the AD Corps had almost doubled in size with dental centres in garrisons across the UK and
                overseas as well as teams in military hospitals and field medical units. The Second World War was to see
                an over ten-fold increase in the Corps’ size, at its height rising to 2143 dental officers, 3833
                soldiers and 267 Auxiliary Territorial Service as dental clerk-orderlies in the Home Commands. The
                dental officer strength represented one in every seven dentists registered in the UK at the time.
